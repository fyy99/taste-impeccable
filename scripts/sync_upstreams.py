class SyncError(RuntimeError):
    """可行动的同步错误。"""


def sha256(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def run_git(args: list[str], *, cwd: Path | None = None) -> str:
    result = subprocess.run(
        ["git", *args],
        cwd=cwd,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if result.returncode != 0:
        command = "git " + " ".join(args)
        detail = result.stderr.strip() or result.stdout.strip()
        raise SyncError(f"{command} 失败：{detail}")
    return result.stdout


def load_lock() -> dict[str, Any]:
    try:
        lock = json.loads(LOCK_PATH.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as exc:
        raise SyncError(f"无法读取 {LOCK_PATH}: {exc}") from exc
    if lock.get("schema_version") != 1:
        raise SyncError("不支持的 upstreams.lock.json schema_version")
    if lock.get("policy", {}).get("orchestration_is_manual") is not True:
        raise SyncError("锁文件必须明确 orchestration_is_manual=true")
    return lock


def save_lock(lock: dict[str, Any]) -> None:
    LOCK_PATH.write_text(
        json.dumps(lock, ensure_ascii=False, indent=2, sort_keys=False) + "\n",
        encoding="utf-8",
    )


def resolve_ref(repository: str, ref: str) -> str:
    """解析 ref；对 annotated tag 必须使用 peeled commit，而不是 tag object。"""
    query = [ref]
    if ref.startswith("refs/tags/"):
        query.append(f"{ref}^{{}}")
    output = run_git(["ls-remote", repository, *query])
    entries: dict[str, str] = {}
    for line in output.splitlines():
        commit, name = line.split(maxsplit=1)
        entries[name] = commit
    peeled = entries.get(f"{ref}^{{}}")
    commit = peeled or entries.get(ref)
    if not commit or not HEX_COMMIT.fullmatch(commit):
        raise SyncError(f"无法解析 {repository} 的 {ref}")
    return commit


def semantic_tag_key(tag: str) -> tuple[int, ...] | None:
    match = re.fullmatch(r"skill-v(\d+(?:\.\d+)*)", tag)
    if not match:
        return None
    return tuple(int(piece) for piece in match.group(1).split("."))


def resolve_latest_release(repository: str, pattern: str) -> tuple[str, str]:
    output = run_git(["ls-remote", "--tags", repository, pattern, f"{pattern}^{{}}"])
    raw: dict[str, str] = {}
    peeled: dict[str, str] = {}
    for line in output.splitlines():
        commit, full_ref = line.split(maxsplit=1)
        short = full_ref.removeprefix("refs/tags/")
        if short.endswith("^{}"):
            peeled[short[:-3]] = commit
        else:
            raw[short] = commit
    candidates = [
        (semantic_tag_key(tag), tag)
        for tag in raw
        if fnmatch.fnmatch(tag, pattern) and semantic_tag_key(tag) is not None
    ]
    if not candidates:
        raise SyncError(f"未找到匹配 {pattern} 的稳定 Skill tag")
    _, tag = max(candidates)
    return tag, peeled.get(tag, raw[tag])


class GitSnapshot:
    def __init__(self, repository: str, commit: str) -> None:
        if not HEX_COMMIT.fullmatch(commit):
            raise SyncError(f"非法 commit: {commit}")
        self.repository = repository
        self.commit = commit
        self._temp = tempfile.TemporaryDirectory(prefix="taste-impeccable-sync-")
        self.path = Path(self._temp.name)
        run_git(["init", "--quiet"], cwd=self.path)
        run_git(["remote", "add", "origin", repository], cwd=self.path)
        run_git(["fetch", "--quiet", "--depth=1", "origin", commit], cwd=self.path)
        fetched = run_git(["rev-parse", "FETCH_HEAD^{commit}"], cwd=self.path).strip()
        if fetched != commit:
            raise SyncError(f"获取到的 commit 不匹配：期望 {commit}，实际 {fetched}")

    def read(self, source_path: str) -> bytes:
        result = subprocess.run(
            ["git", "show", f"{self.commit}:{source_path}"],
            cwd=self.path,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            check=False,
        )
        if result.returncode != 0:
            detail = result.stderr.decode("utf-8", errors="replace").strip()
            raise SyncError(f"{source_path} 在 {self.commit} 不存在：{detail}")
        return result.stdout

    def close(self) -> None:
        self._temp.cleanup()


def markdown_header(
    *,
    upstream: str,
    web_repository: str,
    commit: str,
    source_path: str,
    source_digest: str,
    transform: str,
    license_id: str,
) -> str:
    source_url = f"{web_repository}/blob/{commit}/{source_path}"
    return (
        "<!--\n"
        f"{GENERATED_MARKER}; DO NOT EDIT.\n"
        f"upstream: {upstream}\n"
        f"source: {source_url}\n"
        f"commit: {commit}\n"
        f"source_sha256: {source_digest}\n"
        f"transform: {transform}\n"
        f"SPDX-License-Identifier: {license_id}\n"
        "-->\n\n"
    )


