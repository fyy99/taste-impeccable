def generate(
    lock: dict[str, Any],
) -> tuple[dict[Path, bytes], dict[str, str], dict[Path, bytes]]:
    upstreams = lock["upstreams"]
    snapshots: dict[str, GitSnapshot] = {}
    source_hashes: dict[str, str] = {}
    attribution_files: dict[Path, bytes] = {}
    try:
        for name in ("taste", "impeccable"):
            config = upstreams[name]
            snapshots[name] = GitSnapshot(config["repository"], config["commit"])
        taste = upstreams["taste"]
        impeccable = upstreams["impeccable"]
        generated = {}
        generated.update(generate_taste(taste, snapshots["taste"], source_hashes))
        generated.update(
            generate_impeccable_references(
                impeccable, snapshots["impeccable"], source_hashes
            )
        )
        generated.update(
            generate_impeccable_detector(
                impeccable, snapshots["impeccable"], source_hashes
            )
        )
        for name, config in upstreams.items():
            snapshot = snapshots[name]
            for key in ("license_path", "notice_path"):
                source_path = config.get(key)
                if source_path:
                    content = snapshot.read(source_path)
                    source_hashes[f"{name}:{source_path}"] = sha256(content)
                    target = ATTRIBUTION_TARGETS.get((name, key))
                    if target is None:
                        raise SyncError(f"缺少归属文件目标映射：{name}.{key}")
                    attribution_files[target] = content
        return (
            generated,
            dict(sorted(source_hashes.items())),
            attribution_files,
        )
    finally:
        for snapshot in snapshots.values():
            snapshot.close()


def artifact_records(generated: dict[Path, bytes]) -> dict[str, dict[str, str]]:
    records = {}
    for path, content in sorted(generated.items(), key=lambda item: str(item[0])):
        relative = path.relative_to(ROOT).as_posix()
        records[relative] = {"sha256": sha256(content)}
    return records


def write_generated(generated: dict[Path, bytes]) -> None:
    expected = set(generated)
    managed_roots = [
        REFERENCES_ROOT / "taste",
        REFERENCES_ROOT / "impeccable",
        SCRIPTS_ROOT / "detector",
    ]
    for managed_root in managed_roots:
        if not managed_root.exists():
            continue
        for path in managed_root.rglob("*"):
            if not path.is_file() or path in expected:
                continue
            try:
                prefix = path.read_bytes()[:4096].decode("utf-8", errors="ignore")
            except OSError:
                continue
            if GENERATED_MARKER in prefix:
                path.unlink()
    # detect.mjs 不在 detector 目录，也由本脚本明确管理。
    detect_path = SCRIPTS_ROOT / "detect.mjs"
    if detect_path.exists() and detect_path not in expected:
        prefix = detect_path.read_text(encoding="utf-8", errors="ignore")[:4096]
        if GENERATED_MARKER in prefix:
            detect_path.unlink()

    for path, content in generated.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_bytes(content)


def notice_pin_block(lock: dict[str, Any]) -> str:
    labels = {"taste": "Taste", "impeccable": "Impeccable"}
    lines = [NOTICE_PIN_START]
    for name in ("taste", "impeccable"):
        config = lock["upstreams"][name]
        lines.append(
            f"- {labels[name]}：`{config['ref']}` → `{config['commit']}`"
        )
    lines.append(NOTICE_PIN_END)
    return "\n".join(lines)


def replace_notice_pin_block(text: str, lock: dict[str, Any]) -> str:
    start = text.find(NOTICE_PIN_START)
    end = text.find(NOTICE_PIN_END)
    if start < 0 or end < start:
        raise SyncError("THIRD_PARTY_NOTICES.md 缺少上游 pin 生成区块")
    end += len(NOTICE_PIN_END)
    return text[:start] + notice_pin_block(lock) + text[end:]


def write_attribution(
    lock: dict[str, Any], attribution_files: dict[Path, bytes]
) -> None:
    if set(attribution_files) != set(ATTRIBUTION_TARGETS.values()):
        raise SyncError("许可证/NOTICE 白名单不完整")
    for path, content in attribution_files.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_bytes(content)
    notices_path = ROOT / "THIRD_PARTY_NOTICES.md"
    notices = notices_path.read_text(encoding="utf-8")
    notices_path.write_text(
        replace_notice_pin_block(notices, lock),
        encoding="utf-8",
    )


def check_attribution(
    lock: dict[str, Any], attribution_files: dict[Path, bytes]
) -> None:
    errors: list[str] = []
    for path, expected in attribution_files.items():
        if not path.is_file():
            errors.append(f"缺少归属文件：{path.relative_to(ROOT)}")
        elif path.read_bytes() != expected:
            errors.append(f"归属文件漂移：{path.relative_to(ROOT)}")
    notices_path = ROOT / "THIRD_PARTY_NOTICES.md"
    if not notices_path.is_file():
        errors.append("缺少 THIRD_PARTY_NOTICES.md")
    else:
        notices = notices_path.read_text(encoding="utf-8")
        if replace_notice_pin_block(notices, lock) != notices:
            errors.append("THIRD_PARTY_NOTICES.md 上游 pin 区块漂移")
    if errors:
        raise SyncError("\n".join(errors))


def check_generated(
    lock: dict[str, Any],
    generated: dict[Path, bytes],
    source_hashes: dict[str, str],
) -> None:
    errors: list[str] = []
    for path, expected in generated.items():
        if not path.exists():
            errors.append(f"缺少生成文件：{path.relative_to(ROOT)}")
            continue
        actual = path.read_bytes()
        if actual != expected:
            errors.append(f"生成文件漂移：{path.relative_to(ROOT)}")
    expected_records = artifact_records(generated)
    if lock.get("artifacts") != expected_records:
        errors.append("锁文件 artifacts 与固定提交生成结果不一致")
    if lock.get("source_hashes") != source_hashes:
        errors.append("锁文件 source_hashes 与固定提交不一致")
    if errors:
        raise SyncError("\n".join(errors) + "\n请审阅后运行 --update。")


def update_pins(lock: dict[str, Any]) -> list[str]:
    changes: list[str] = []
    for name, config in lock["upstreams"].items():
        resolved = resolve_ref(config["repository"], config["ref"])
        previous = config["commit"]
        if resolved != previous:
            changes.append(f"{name}: {previous} -> {resolved}")
            config["commit"] = resolved
    return changes


def command_update(lock: dict[str, Any]) -> None:
    previous_hashes = lock.get("source_hashes")
    previous_artifacts = lock.get("artifacts")
    review_state_missing = "pending_review" not in lock.get("policy", {})
    changes = update_pins(lock)
    generated, source_hashes, attribution_files = generate(lock)
    records = artifact_records(generated)
    write_generated(generated)
    write_attribution(lock, attribution_files)
    lock["source_hashes"] = source_hashes
    lock["artifacts"] = records
    content_changed = (
        review_state_missing
        or bool(changes)
        or previous_hashes != source_hashes
        or previous_artifacts != records
    )
    if content_changed:
        lock["policy"]["pending_review"] = True
        lock["reviewed_at"] = None
    save_lock(lock)
    if changes:
        print("已更新上游固定点：")
        for change in changes:
            print(f"- {change}")
        print("编排规则未改写；必须人工审阅生成差异。")
    print(
        f"已同步 {len(generated)} 个白名单产物及第三方归属文件；编排规则未改写。"
    )
    if content_changed:
        print(
            "锁文件已标记 pending_review=true；人工审阅 diff 后运行 --accept-review。"
        )


def command_check(lock: dict[str, Any]) -> None:
    generated, source_hashes, attribution_files = generate(lock)
    check_generated(lock, generated, source_hashes)
    check_attribution(lock, attribution_files)
    print(
        f"上游同步校验通过：{len(generated)} 个白名单产物、许可证和 NOTICE "
        "与锁文件一致。"
    )


def command_accept_review(lock: dict[str, Any]) -> None:
    generated, source_hashes, attribution_files = generate(lock)
    check_generated(lock, generated, source_hashes)
    check_attribution(lock, attribution_files)
    lock["policy"]["pending_review"] = False
    lock["reviewed_at"] = dt.date.today().isoformat()
    save_lock(lock)
    print(
        f"已记录人工审阅：reviewed_at={lock['reviewed_at']}，pending_review=false。"
    )


def command_check_updates(lock: dict[str, Any]) -> int:
    updates: list[str] = []
    for name, config in lock["upstreams"].items():
        if name == "impeccable" and config.get("release_tag_pattern"):
            latest_tag, latest_commit = resolve_latest_release(
                config["repository"], config["release_tag_pattern"]
            )
            current_tag = config["ref"].removeprefix("refs/tags/")
            if latest_tag != current_tag or latest_commit != config["commit"]:
                updates.append(
                    f"impeccable: {current_tag}@{config['commit']} -> "
                    f"{latest_tag}@{latest_commit}"
                )
        else:
            latest_commit = resolve_ref(config["repository"], config["ref"])
            if latest_commit != config["commit"]:
                updates.append(
                    f"{name}: {config['commit']} -> {latest_commit} ({config['ref']})"
                )
    if updates:
        print("发现上游更新：")
        for update in updates:
            print(f"- {update}")
        print("先人工更新 ref/审阅差异，再运行 --update；编排不会自动变化。")
        return 3
    print("未发现上游更新。")
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--check", action="store_true", help="从锁定提交重建并只读比对")
    mode.add_argument(
        "--check-updates", action="store_true", help="只读检查跟踪分支/稳定 tag 更新"
    )
    mode.add_argument(
        "--update", action="store_true", help="解析配置 ref、同步白名单并更新哈希"
    )
    mode.add_argument(
        "--accept-review",
        action="store_true",
        help="在人工审阅 diff 后记录日期并解除 pending_review 门禁",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    try:
        lock = load_lock()
        if args.check:
            command_check(lock)
        elif args.check_updates:
            return command_check_updates(lock)
        elif args.accept_review:
            command_accept_review(lock)
        else:
            command_update(lock)
        return 0
    except (SyncError, OSError, UnicodeError) as exc:
        print(f"错误：{exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
