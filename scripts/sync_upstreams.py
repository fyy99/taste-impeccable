def with_script_header(content: bytes, header: str) -> bytes:
    text = content.decode("utf-8")
    if text.startswith("#!"):
        first, separator, rest = text.partition("\n")
        return f"{first}\n{header}{rest}".encode()
    return (header + text).encode()


